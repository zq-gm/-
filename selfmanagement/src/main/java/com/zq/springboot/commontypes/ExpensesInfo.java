package com.zq.springboot.commontypes;

import java.util.Date;

public class ExpensesInfo {

	private String id;
	private String expensesName;
	private double expensesAmount;
	private String familyId;
	private String expensesPeopleId;
	private String expensesPeople;
	private String expensesDate;
	private String expensesType;
	private String payType;
	private String remarks;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getExpensesName() {
		return expensesName;
	}
	public void setExpensesName(String expensesName) {
		this.expensesName = expensesName;
	}
	public double getExpensesAmount() {
		return expensesAmount;
	}
	public void setExpensesAmount(double expensesAmount) {
		this.expensesAmount = expensesAmount;
	}
	public String getFamilyId() {
		return familyId;
	}
	public void setFamilyId(String familyId) {
		this.familyId = familyId;
	}
	public String getExpensesPeopleId() {
		return expensesPeopleId;
	}
	public void setExpensesPeopleId(String expensesPeopleId) {
		this.expensesPeopleId = expensesPeopleId;
	}
	public String getExpensesPeople() {
		return expensesPeople;
	}
	public void setExpensesPeople(String expensesPeople) {
		this.expensesPeople = expensesPeople;
	}
	public String getExpensesDate() {
		return expensesDate;
	}
	public void setExpensesDate(String expensesDate) {
		this.expensesDate = expensesDate;
	}
	public String getExpensesType() {
		return expensesType;
	}
	public void setExpensesType(String expensesType) {
		this.expensesType = expensesType;
	}
	public String getPayType() {
		return payType;
	}
	public void setPayType(String payType) {
		this.payType = payType;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	
	
}
